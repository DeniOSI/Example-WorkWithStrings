public class PrintS {
    public static void Print(String[] str_a)
    {
        for (String value: str_a ) {
            System.out.println(value);

        }
    }
    public static void Print(char[] char_a)
    {
        for (char value: char_a ) {
            System.out.println("symbol = " +  value);

        }
    }
}
